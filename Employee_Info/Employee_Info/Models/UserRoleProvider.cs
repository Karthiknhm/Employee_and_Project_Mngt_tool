        public override string[] GetRolesForUser(string username)
        {
            using (UserRoleContext _Context = new UserRoleContext())
            {
                var nRoleId = _Context.Tb_M_User.Where(u => u.UserName == username).SingleOrDefault().UsRoleId;
                string userRoleNames = _Context.Tb_M_Role.Where(u => u.RoleId == nRoleId).SingleOrDefault().RoleName;

                var userRoles = new string[] { userRoleNames };
                return userRoles;
            }
